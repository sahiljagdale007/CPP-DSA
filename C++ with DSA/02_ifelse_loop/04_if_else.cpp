// Progrma which tells us which number is greater!!!
#include <iostream>
using namespace std;
int main()
{
    int a, b;

    cout << "Enter the value of a " << endl;
    cin >> a;
    cout << "Enter the value of b " << endl;
    cin >> b;

    if (a > b)
    {
        cout << "A is greater " << endl;
    }
    if (a < b)
    {
        cout << "B is greater " << endl;
    }
}
